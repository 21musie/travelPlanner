function Location(id, title, location, category, rating, description, imageUrls, createdAt, author) {
    this.id = id;
    this.title = title;
    this.location = location;
    this.category = category;
    this.rating = rating;
    this.description = description;
    this.imageUrls = imageUrls;
    this.createdAt = createdAt;
    this.author = author;
}